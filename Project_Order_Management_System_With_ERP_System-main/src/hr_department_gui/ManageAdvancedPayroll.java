    private void jTable1KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTable1KeyPressed
        
        int row = jTable1.getSelectedRow();
        
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            
            if (row == -1) {
                
                JOptionPane.showMessageDialog(this, "Please select a row to update advance status or note", "Empty row selection", JOptionPane.WARNING_MESSAGE);
                
            }else{
                
                String payID = String.valueOf(jTable1.getValueAt(row, 0));                
                String advanceNote = String.valueOf(jTable1.getValueAt(row, 6));
                String employeeName = String.valueOf(jTable1.getValueAt(row, 2));
                
                try {
                    
                    MySql.executeUpdate("UPDATE `advanced_payroll` SET `advance_status_or_note` = '"+advanceNote+"' WHERE `ad_pay_id` = '"+payID+"' ");

                    JOptionPane.showMessageDialog(this, "Successfully add note to"+employeeName, "Note adding success", JOptionPane.INFORMATION_MESSAGE);
                    
                    loadDeatils();
                    
                    clear();
                    
                } catch (Exception e) {
                    
                    e.printStackTrace();
                    
                }                
                
            }
            
        }
        
        
    }//GEN-LAST:event_jTable1KeyPressed

    private void printReportButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_printReportButtonActionPerformed
      
        try {
            // Path to the compiled Jasper report
            String reportPath = "src/report/advanced_payroll.jasper";

            // Create parameters map (if needed)
            HashMap<String, Object> parameters = new HashMap<>();

            // Create data source from JTable model
            JRTableModelDataSource dataSource = new JRTableModelDataSource(jTable1.getModel());

            // Fill the report
            JasperPrint jasperPrint = JasperFillManager.fillReport(reportPath, parameters, dataSource);

            // Show the report in viewer
            JasperViewer.viewReport(jasperPrint, false);

        } catch (JRException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error generating report: " + e.getMessage(), "Report Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_printReportButtonActionPerformed

