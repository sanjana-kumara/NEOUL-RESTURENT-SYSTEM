    private void loadDeatils(){
        
        try {
            
            model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);
            
            ResultSet rs = MySql.executeSearch("SELECT * FROM `advanced_payroll` INNER JOIN `employee` ON "
                    + "`advanced_payroll`.`employee_employee_id` = `employee`.`employee_id` INNER JOIN `employee_payrolls`"
                    + " ON `employee_payrolls`.`payroll_id` = `advanced_payroll`.`employee_payrolls_payroll_id` ");
            
            while (rs.next()) {                
                
                Vector<String> vector = new Vector<>();
                
                vector.add(rs.getString("ad_pay_id"));
                vector.add(rs.getString("employee.employee_id"));
                vector.add(rs.getString("employee_name"));
                vector.add(rs.getString("date"));
                vector.add(rs.getString("advanced_price"));
                vector.add(rs.getString("employee_payrolls.payable_salary"));
                vector.add(rs.getString("advance_status_or_note"));
                
                model.addRow(vector);
                
            }
            
            
            
        } catch (Exception e) {
            
            e.printStackTrace();
            
        }
        
    }

